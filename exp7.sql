CREATE TABLE CUSTOMERS007( 
CUSTID INT PRIMARY KEY,
CUSTNAME VARCHAR(20),
ADDRESS VARCHAR(20),
STATE VARCHAR(20)

);

CREATE TABLE ORDERS007(
ORDERID VARCHAR(20) PRIMARY KEY,
ITEMID VARCHAR(20) REFERENCES  ITEM(ITEMID),
CUSTID INT REFERENCES CUSTOMERS007(CUSTID),
QUANTITY VARCHAR(20) CHECK (QUANTITY>=0),
ORDERDATE DATE
);

CREATE TABLE DELHIVERY(
DELHIVERYID VARCHAR(20) PRIMARY KEY,
CUSTID INT REFERENCES CUSTOMERS007(CUSTID),
ORDERID VARCHAR(20) REFERENCES ORDERS007(ORDERID)
);

